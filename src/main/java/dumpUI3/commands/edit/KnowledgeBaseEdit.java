package dumpUI3.commands.edit;






/**
 *
 * @author mjcobo
 */
public abstract class KnowledgeBaseEdit {

  /***************************************************************************/
  /*                        Private attributes                               */
  /***************************************************************************/

  /**
   *
   */
  protected String errorMessage;

  /***************************************************************************/
  /*                            Constructors                                 */
  /***************************************************************************/

  public KnowledgeBaseEdit() {
    super();

    this.errorMessage = "";
  }

  /***************************************************************************/
  /*                           Public Methods                                */
  /***************************************************************************/

  /**
   *
   * @return
   */
  public String getErrorMessage() {
    return errorMessage;
  }

  /**
   *
   */
  public abstract boolean execute() ;

  
 

  /***************************************************************************/
  /*                           Private Methods                               */
  /***************************************************************************/
}